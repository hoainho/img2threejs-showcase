#!/usr/bin/env node
// Scaffolds a new showcase demo: creates src/demos/<id>/, a stub factory,
// and inserts a DemoEntry into src/demos/registry.ts. Plain Node, no deps.
//
// Usage:
//   npm run new-demo -- <id> "<Title>" [object|character]
//
// Example:
//   npm run new-demo -- rubber-duck "Rubber Duck" object

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));

function fail(message) {
  console.error(`\nnew-showcase: ${message}\n`);
  process.exit(1);
}

function toPascalCase(id) {
  return id
    .split('-')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join('');
}

function main() {
  const [id, title, subjectClassArg] = process.argv.slice(2);

  if (!id) {
    fail('missing <id>. Usage: npm run new-demo -- <id> "<Title>" [object|character]');
  }
  if (!/^[a-z0-9]+(-[a-z0-9]+)*$/.test(id)) {
    fail(`"${id}" is not kebab-case (lowercase letters, digits, hyphens only).`);
  }

  const subjectClass = subjectClassArg === 'character' ? 'character' : 'object';
  const displayTitle = title || toPascalCase(id).replace(/([a-z])([A-Z])/g, '$1 $2');
  const pascalId = toPascalCase(id);
  const factoryFnName = `create${pascalId}Model`;
  const demoDir = join(ROOT, 'src/demos', id);
  const factoryFile = join(demoDir, `${factoryFnName}.ts`);
  const registryFile = join(ROOT, 'src/demos/registry.ts');

  if (existsSync(demoDir)) {
    fail(`src/demos/${id}/ already exists — pick a different id, or edit it directly.`);
  }
  if (!existsSync(registryFile)) {
    fail(`could not find ${registryFile} — run this from the repo root.`);
  }

  const registrySource = readFileSync(registryFile, 'utf8');
  if (new RegExp(`id:\\s*'${id}'`).test(registrySource)) {
    fail(`registry.ts already has an entry with id "${id}".`);
  }

  // 1. Stub factory — a placeholder box so `npm run build` and the demo route
  //    work immediately. Replace the body with your img2threejs output.
  mkdirSync(demoDir, { recursive: true });
  writeFileSync(
    factoryFile,
    `import * as THREE from 'three';

// TODO: replace this placeholder with the factory generated by img2threejs.
// Keep the exported function name (${factoryFnName}) so the registry import
// below keeps working, or update both if you rename it.
export function ${factoryFnName}(): THREE.Group {
  const group = new THREE.Group();
  const mesh = new THREE.Mesh(
    new THREE.BoxGeometry(1, 1, 1),
    new THREE.MeshStandardMaterial({ color: 0x888888, roughness: 0.6 }),
  );
  mesh.castShadow = true;
  group.add(mesh);
  return group;
}
`,
  );

  // 2. Insert the import + registry entry.
  const importLine = `import { ${factoryFnName} } from './${id}/${factoryFnName}';\n`;
  const withImport = registrySource.replace(
    /(^import \* as THREE from 'three';\n)/,
    `$1${importLine}`,
  );

  const entryBlock = `  {
    id: '${id}',
    title: '${displayTitle.replace(/'/g, "\\'")}',
    subjectClass: '${subjectClass}',
    blurb:
      'TODO: 1-2 sentences describing what makes this reconstruction interesting.',
    referenceImage: \`\${BASE}references/${id}.png\`,
    sourcePath: 'src/demos/${id}/${factoryFnName}.ts',
    sourceUrl: \`\${REPO}/src/demos/${id}/${factoryFnName}.ts\`,
    generatedWith: 'img2threejs vX.Y', // TODO: fill in the version you used
    author: 'TODO: your display name',
    authorUrl: 'TODO: https://github.com/your-username',
    status: 'placeholder', // set to 'final' once the real factory is in
    cameraPosition: [3, 2, 4],
    cameraTarget: [0, 0, 0],
    cameraFov: 35,
    build: (scene) => {
      const group = ${factoryFnName}();
      scene.add(group);
      return group;
    },
  },
`;

  const withEntry = withImport.replace(
    /(export const demos: DemoEntry\[\] = \[\n)/,
    `$1${entryBlock}`,
  );

  if (withEntry === withImport) {
    fail('could not find "export const demos: DemoEntry[] = [" in registry.ts — insert the entry manually.');
  }

  writeFileSync(registryFile, withEntry);

  console.log(`
Scaffolded "${id}":
  - src/demos/${id}/${factoryFnName}.ts       (placeholder box — replace with img2threejs output)
  - src/demos/registry.ts                      (new entry added, marked TODO)

Next steps:
  1. Replace the placeholder factory body with your generated code.
  2. Fill in every TODO in the new registry.ts entry (blurb, generatedWith, author, authorUrl).
  3. Add public/references/${id}.png (or .jpg/.jpeg/.webp, <= 800 KB).
  4. npm run build && npm run preview   — check #/demo/${id}
  5. node scripts/check-showcase-safety.mjs --base main
  6. git add -A && git commit -m "Add ${id} showcase demo" && git push
  7. gh pr create --fill   (or open https://github.com/hoainho/img2threejs-showcase/compare)
`);
}

main();
